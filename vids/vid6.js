console.log('Operators in Js');
// Arithmetic Operators
let a = 10;
let b = 4;
console.log('a + b = ', a + b);
console.log('a - b = ', a - b);
console.log('a / b = ', a / b);
console.log('a ** b = ', a ** b);
console.log('a % b = ', a % b);
console.log('++a = ', ++a);
console.log('a++ = ', a++);
console.log('--a = ', --a);
console.log('a-- = ', a--);
console.log('a = ', a);
console.log('a-- = ', a--);

// Assignment Operator
// Assignment Operators
let assignment = 1;
assignment += 5; // same as assignment = assignment + 5
console.log('a is now = ', a);
assignment -= 5; // same as assignment = assignment - 5
console.log('a is now = ', a);
assignment *= 5; // same as assignment = assignment * 5
console.log('a is now = ', a);
assignment /= 5; // same as assignment = assignment / 5
console.log('a is now = ', a);

// Comparison Operator
let comp1 = 6;
let comp2 = 7;
console.log('comp1 == comp2 : ', comp1 == comp2);
console.log('comp1 != comp2 : ', comp1 != comp2);
console.log('comp1 === comp2 : ', comp1 === comp2);
console.log('comp1 !== comp2 : ', comp1 !== comp2);
console.log('comp1 <= comp2 : ', comp1 <= comp2);

// Logical Operators
let x = 5;
let y = 6;
console.log(x < y && x == 5);
console.log(x > y || x == 5);
console.log(!false);
console.log(!true);
